import { homedir } from "node:os";
import { existsSync } from "node:fs";
import { join } from "node:path";
import { writeFile, mkdir } from "node:fs/promises";
import type { ProviderAdapter, ProviderInfo, GeneratedFile } from "./types.js";
import type { McpServerDefinition, McpServerConfig } from "../mcp/types.js";
import type { AgentDefinition } from "../agents/types.js";
import { commandExists, run, runSilent } from "../utils/shell.js";
import { log } from "../utils/logger.js";

const GENERATED_HEADER = "<!-- Generated by agent-stack. Do not edit manually. -->";

export class ClaudeCodeAdapter implements ProviderAdapter {
  id = "claude-code";
  displayName = "Claude Code";

  async detect(): Promise<ProviderInfo> {
    const home = homedir();
    const configPaths = [
      join(home, ".claude"),
      join(home, ".config", "claude"),
    ];

    const installed = await commandExists("claude");
    let version: string | undefined;

    if (installed) {
      const result = await runSilent("claude", ["--version"]);
      if (result.exitCode === 0) {
        version = result.stdout.trim();
      }
    }

    return {
      name: this.id,
      displayName: this.displayName,
      installed,
      version,
      configPaths: configPaths.filter((p) => existsSync(p)),
    };
  }

  async install(): Promise<void> {
    log.step("Installing Claude Code via npm...");
    const result = await run("npm", ["install", "-g", "@anthropic-ai/claude-code"]);
    if (result.exitCode !== 0) {
      throw new Error(`Failed to install Claude Code: ${result.stderr}`);
    }
    log.success("Claude Code installed successfully");
  }

  async configureMcp(
    servers: McpServerDefinition[],
    envValues: Record<string, string>
  ): Promise<void> {
    for (const server of servers) {
      const args = ["mcp", "add", server.name, "--transport", server.transport];

      if (server.command) {
        args.push("--", server.command);
      }
      if (server.args) {
        args.push(...server.args);
      }

      const env: Record<string, string> = {};
      if (server.env) {
        for (const key of Object.keys(server.env)) {
          if (envValues[key]) {
            env[key] = envValues[key];
          }
        }
      }

      const envArgs: string[] = [];
      for (const [key, value] of Object.entries(env)) {
        envArgs.push("-e", `${key}=${value}`);
      }

      const fullArgs = [...args, ...envArgs];
      const result = await run("claude", fullArgs);
      if (result.exitCode !== 0) {
        log.warn(`Failed to add MCP server "${server.name}": ${result.stderr}`);
      } else {
        log.success(`MCP server "${server.displayName}" configured`);
      }
    }
  }

  async removeMcp(serverName: string): Promise<void> {
    const result = await run("claude", ["mcp", "remove", serverName]);
    if (result.exitCode !== 0) {
      throw new Error(`Failed to remove MCP server "${serverName}": ${result.stderr}`);
    }
  }

  async listMcp(): Promise<McpServerConfig[]> {
    const result = await runSilent("claude", ["mcp", "list"]);
    if (result.exitCode !== 0) {
      return [];
    }

    const lines = result.stdout.trim().split("\n").filter(Boolean);
    return lines.map((line) => {
      const parts = line.split(/\s+/);
      return {
        name: parts[0] ?? line,
        command: parts[1] ?? "",
        args: parts.slice(2),
      };
    });
  }

  async generateAgentConfig(
    agents: AgentDefinition[],
    projectDir: string
  ): Promise<GeneratedFile[]> {
    const files: GeneratedFile[] = [];

    const relevantAgents = agents.filter(
      (a) => !a.providers || a.providers.includes("claude-code")
    );

    if (relevantAgents.length === 0) return files;

    // Generate CLAUDE.md
    const sections: string[] = [GENERATED_HEADER, ""];

    for (const agent of relevantAgents) {
      sections.push(`## ${agent.name}`);
      sections.push("");

      if (agent.systemPrompt) {
        sections.push(agent.systemPrompt);
        sections.push("");
      }

      if (agent.rules && agent.rules.length > 0) {
        sections.push("### Rules");
        sections.push("");
        for (const rule of agent.rules) {
          sections.push(`- ${rule}`);
        }
        sections.push("");
      }
    }

    files.push({
      path: "CLAUDE.md",
      content: sections.join("\n"),
      action: existsSync(join(projectDir, "CLAUDE.md")) ? "overwrite" : "create",
    });

    // Generate hooks in .claude/settings.json if any agent has hooks
    const allHooks = relevantAgents.flatMap((a) => a.hooks ?? []);
    if (allHooks.length > 0) {
      const hooksConfig: Record<string, string[]> = {};
      for (const hook of allHooks) {
        const key = hook.event.replace(":", "_");
        if (!hooksConfig[key]) hooksConfig[key] = [];
        hooksConfig[key].push(hook.command);
      }

      files.push({
        path: ".claude/settings.json",
        content: JSON.stringify({ hooks: hooksConfig }, null, 2),
        action: existsSync(join(projectDir, ".claude", "settings.json"))
          ? "overwrite"
          : "create",
      });
    }

    return files;
  }
}
